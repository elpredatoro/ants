package pl.elpredatoro.ants;

import java.util.Date;

public class Marker {
	private int x;
	private int y;
	
	public Marker(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
